    private void cargarTabla() {
        //Hacemos que la table no sea editable
        DefaultTableModel modeloTabla = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int colum) {
                return false;
            }
        };

        //ponemos titulos a las columnas
        String titulos[] = {"ID", "MODELO", "MARCA", "MOTOR", "COLOR", "PLACA", "N# PUERTAS"};
        modeloTabla.setColumnIdentifiers(titulos);

        //traer los autos desde la base de datos
        List<Automovil> listaAutomoviles = control.traerAuto();

        //setear los datos en la tabla
        if (listaAutomoviles != null) {
            for (Automovil auto : listaAutomoviles) {
                Object[] object = {auto.getId(), auto.getModelo(), auto.getMarca(), auto.getMotor(), auto.getColor(), auto.getPlaca(), auto.getPuertas()};

                modeloTabla.addRow(object);
            }
        }

        tablaAutos.setModel(modeloTabla);

    }
