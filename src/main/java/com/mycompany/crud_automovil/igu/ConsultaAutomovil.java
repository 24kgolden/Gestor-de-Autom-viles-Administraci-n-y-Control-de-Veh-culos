    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //Controlamos que la tabala no este vacia 
        if (tablaAutos.getRowCount() > 0) {
            //validamos que se haya selecioando un registro
            if (tablaAutos.getSelectedRow() != -1) {
                //obetener la id del auto que quiero eliminar
                int idAuto = Integer.parseInt(String.valueOf(tablaAutos.getValueAt(tablaAutos.getSelectedRow(), 0)));

                control.borrarAuto(idAuto);
                mostarMensaje("Auto borrado correctamente", "Info", "Borrado Exitoso");
                cargarTabla();
            } else {
                mostarMensaje("No seleccionó registro a eliminar", "Error", "Error al eliminar");
            }
        } else {
            mostarMensaje("La tabla esta vacía, no se puede eliminar", "Error", "Error al eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if (tablaAutos.getRowCount() > 0) {
            //validamos que se haya selecioando un registro
            if (tablaAutos.getSelectedRow() != -1) {
                 //obetener la id del auto que quiero modificar
                int idAuto = Integer.parseInt(String.valueOf(tablaAutos.getValueAt(tablaAutos.getSelectedRow(), 0)));
                ModifAuto modif = new ModifAuto(idAuto);
                modif.setVisible(true);
                modif.setLocationRelativeTo(null);
                //cierra la ventana
                this.dispose();
            } else {
                mostarMensaje("No seleccionó registro para modificar", "Error", "Error al modificar");
            }
        } else {
            mostarMensaje("La tabla esta vacía, no se puede modificar", "Error", "Error al modificar");
        }

    }//GEN-LAST:event_btnModificarActionPerformed

    public void mostarMensaje(String mensaje, String tipo, String titulo) {
        JOptionPane optionPane = new JOptionPane(mensaje);
        if (tipo.equals("Infro")) {
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        } else if (tipo.equals("Error")) {
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }

